    private void btnConsultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConsultarMouseClicked
        // TODO add your handling code here:
        try {
            if(!txtAnio1.equals("") && !txtAnio2.equals("")){
                btnVerDetalle.setEnabled(false);
                int idMateria = Integer.parseInt(cboTemas.getSelectedItem().toString().split(" - ")[0]);
                int anio1 = Integer.parseInt(txtAnio1.getText());
                int anio2 = Integer.parseInt(txtAnio2.getText());
                String palabraClave = txtPalabraClave.getText();
                
                ArrayList<Libro> librosConsultados = libroDAO.consultarLibros(idMateria, anio1, anio2, palabraClave);
                llenarJTableLibrosConsultados(librosConsultados);
            }
            
        } catch (SQLException e) {
           JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (Exception e2) {
            JOptionPane.showMessageDialog(this, "Asegúrese de completar correctamente los campos.");
        }
    }//GEN-LAST:event_btnConsultarMouseClicked

