    public void cargarCboTemas(){
        try {
            listaTemas = temaDAO.obtenerTodosTemas();
            
            for (int i = 0; i < listaTemas.tamanio(); i++) {
                cboTemas.addItem(listaTemas.obtenerTema(i).getId()+ " - " + listaTemas.obtenerTema(i).getDescripcion());
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
    private void llenarJTableLibrosConsultados(ArrayList<Libro> libros){
        
        DefaultTableModel dtm = new DefaultTableModel();
        
        String[] cabecera = {"Código", "Título", "Autor", "Tema"}; 
        dtm.setColumnIdentifiers(cabecera);
        dtm.setColumnCount(cabecera.length);
        Object[] dataTema = new Object[dtm.getColumnCount()];
        
        for (int i = 0; i < libros.size(); i++) {
            dataTema[0] = libros.get(i).getId();
            dataTema[1] = libros.get(i).getTitulo();
            dataTema[2] = libros.get(i).getAutores();
            dataTema[3] = libros.get(i).getDescripcion_materia();
            dtm.addRow(dataTema);
        }
        
        jTableLibrosConsultados.setModel(dtm);
    }
    
