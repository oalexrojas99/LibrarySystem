    private void btnVerDetalleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnVerDetalleMouseClicked
        // TODO add your handling code here:
        
        int filaSeleccionada = 0;
        
        if(jTableLibrosConsultados.getSelectedRow() != -1){
            
            try {
                filaSeleccionada = jTableLibrosConsultados.getSelectedRow();
                
                int idLibro = Integer.parseInt(jTableLibrosConsultados.getValueAt(filaSeleccionada, 0) + "");
                Libro libroConsultado = libroDAO.obtenerLibroXID(idLibro);
                GUIDetallesYSugerencias guiDetalles = new GUIDetallesYSugerencias(libroConsultado);
                this.setVisible(false);
                guiDetalles.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(GUIBusquedaLibros.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnVerDetalleMouseClicked

