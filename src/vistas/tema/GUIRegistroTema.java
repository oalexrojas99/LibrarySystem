    private void btnRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarMouseClicked
        // TODO add your handling code here:
        try {
            if(txtDescripcion.getText().equals(null) || txtDescripcion.getText().equals("")){
                throw new ExcepcionPropia("Falta completar datos en el campo Descripci贸n.");
            }
            
            Tema objMateria = new Tema();

            objMateria.setDescripcion(txtDescripcion.getText().trim());

            temaDAO.registrar(objMateria);
            
            miGrafoBusqueda.crearVertice(objMateria.getDescripcion());
            miGrafoBusqueda.serializarGrafo(miGrafoBusqueda);
            
            JOptionPane.showMessageDialog(this, String.format("Nueva materia %s registrada exitosamente", objMateria.getDescripcion()));
            irAGUIMateria();
            
            establecerPorDefecto();
        } catch (ExcepcionPropia | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarMouseClicked

