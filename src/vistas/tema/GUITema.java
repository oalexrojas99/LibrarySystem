    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        // TODO add your handling code here:
        
        int opcion = JOptionPane.showConfirmDialog(this,"¿Está seguro de que desea eliminar este registro?", "Confirmación de eliminación de materia", JOptionPane.YES_NO_OPTION);
        
        if(opcion == JOptionPane.YES_OPTION){
            
            try {
            Tema objMateria = new Tema();

            objMateria.setId(Integer.parseInt(lblCodigo.getText()));
            objMateria.setDescripcion(txtDescripcion.getText().trim());

            temaDAO.eliminar(objMateria);

            JOptionPane.showMessageDialog(this, String.format("Datos de materia %s eliminados correctamente.", objMateria.getDescripcion()));

            establecerPorDefecto();
            } catch (ExcepcionPropia | SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Asegúrese de selccionar un registro de la tabla.");
            }
        }
        
    }//GEN-LAST:event_btnEliminarMouseClicked

