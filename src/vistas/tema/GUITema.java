    private void llenarJTableMaterias(){
        DefaultTableModel dtm = new DefaultTableModel();
        
        try {
            listaTemas =temaDAO.obtenerTodosTemas();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Hubo un problema al cargar los datos de temas.");
        }
        
        String[] cabecera = {"Código", "Descripción de tema"}; 
        dtm.setColumnIdentifiers(cabecera);
        dtm.setColumnCount(cabecera.length);
        Object[] dataCliente = new Object[dtm.getColumnCount()];
        
        for (int i = 0; i < listaTemas.tamanio(); i++) {
            dataCliente[0] = listaTemas.obtenerTema(i).getId();
            dataCliente[1] = listaTemas.obtenerTema(i).getDescripcion();
            dtm.addRow(dataCliente);
        }
        jTableMaterias.setModel(dtm);
    }
    
