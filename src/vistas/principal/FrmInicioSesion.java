    private void iniciarSesion(String username, String password){
        try {
            boolean credenciales_correctas;
            credenciales_correctas = usuarioDAO.login(username, password);
            
            if(credenciales_correctas){
                this.setVisible(false);
                GUIPrincipal guiPrincipal = new GUIPrincipal();
                guiPrincipal.setVisible(true);
                
            } else{
                throw new ExcepcionPropia("Datos de usuario incorrectos");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            limpiar();
        }
    }
    
