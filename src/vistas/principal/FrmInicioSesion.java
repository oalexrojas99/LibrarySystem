    private void txtClaveKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtClaveKeyPressed
        
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!txtUsuario.equals("") && !String.valueOf(txtClave.getPassword()).equals("")){
                iniciarSesion(txtUsuario.getText(), String.valueOf(txtClave.getPassword()));
            } else{
                JOptionPane.showMessageDialog(this, "Debe ingresar datos en los campos usuario y contraseña");
            }
        }
    }//GEN-LAST:event_txtClaveKeyPressed

